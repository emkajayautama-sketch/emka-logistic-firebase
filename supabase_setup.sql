-- Setup Tabel Master Data untuk SILOP
-- Silakan copy semua kode ini dan jalankan di SQL Editor Supabase

-- 1. Buat Tabel Kapal (Ships)
CREATE TABLE ships (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text NOT NULL,
  name text NOT NULL,
  type text,
  status text DEFAULT 'Aktif',
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Buat Tabel Klien (Clients)
CREATE TABLE clients (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code text NOT NULL,
  name text NOT NULL,
  type text,
  status text DEFAULT 'Aktif',
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

