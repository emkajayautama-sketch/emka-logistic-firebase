-- 4. Buka akses agar bisa dibaca dan ditulis oleh aplikasi (Anon)
-- PERINGATAN: Di tahap produksi, akses ini harus dibatasi dengan Row Level Security (RLS)
ALTER TABLE ships ENABLE ROW LEVEL SECURITY;
ALTER TABLE clients ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow public read access for ships" ON ships FOR SELECT USING (true);
CREATE POLICY "Allow public insert access for ships" ON ships FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public delete access for ships" ON ships FOR DELETE USING (true);

CREATE POLICY "Allow public read access for clients" ON clients FOR SELECT USING (true);
CREATE POLICY "Allow public insert access for clients" ON clients FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public delete access for clients" ON clients FOR DELETE USING (true);
